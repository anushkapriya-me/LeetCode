//Q217. Contains Duplicate

class Solution {
    public boolean containsDuplicate(int[] nums) {
        HashMap<Integer,Integer> map =new HashMap<>();
        for(int i=0;i<=nums.length-1;i++){
            int num=nums[i];
            if (map.containsKey(num)){
                return true;
            }
            map.put(num,1);
        }
        return false;
    }
}